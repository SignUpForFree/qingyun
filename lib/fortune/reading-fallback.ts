function tierOf(score: number): Tier {
  if (score >= 80) return "high";
  if (score >= 65) return "mid";
  return "low";
}

/**
 * 按 (date + dim) 做稳定 hash 选句子，让同一天同一档同一句不抖动
 */
function pick(date: string, dim: DailyDim7, tier: Tier): string {
  const pool = FALLBACK[dim][tier];
  let h = 2166136261;
  const s = `${date}|${dim}|${tier}`;
  for (let i = 0; i < s.length; i++) {
    h ^= s.charCodeAt(i);
    h = Math.imul(h, 16777619);
  }
  return pool[(h >>> 0) % pool.length] ?? "";
}

/**
 * 用各维度 score 拼 7 段 reading
 *   "【爱情 78】今天关系上的小细节会被你接住..."
 */
export function buildReadingFallback(date: string, scores: DimensionScores7): string {
  const dims: ReadonlyArray<DailyDim7> = [
    "爱情",
    "财富",
    "事业",
    "学习",
    "健康",
    "人际",
    "心情",
  ];
  return dims
    .map((dim) => `【${dim} ${scores[dim]}】${pick(date, dim, tierOf(scores[dim]))}`)
    .join("\n\n");
}
